package b_operator;

public class Ex07_이진법_복습 {
	public static void main(String[] args) {
		
		int a = 15;
		int b = 10;
		int and = a & b;
		System.out.println("and=" + and);
		
		int or = a | b;
		System.out.println("or=" + or);
		
		int exor = a ^ b;
		System.out.println("exor=" + exor);
	}

}
